// 1. Дана строка. Проверьте, что эта строка состоит только из цифр.

const str = "123456";

const isNumeric = !isNaN(str) && !str.includes(" ");

console.log(isNumeric); // true

// isNaN(str): Проверяет, является ли строка числом (возвращает false для строки, состоящей только из цифр).
// !str.includes(" "): Исключает строки с пробелами.

/****************************************************************************************/

// 2. Дана строка. Проверьте, что эта строка состоит только из четных цифр.

const str2 = '2468';

function f(inputStr) { // Принимаем строку как параметр
    for (let num of inputStr) {
        if (Number(num) % 2 !== 0) { // Проверяем, является ли число нечётным
            return false; // Если хоть одно нечётное число, возвращаем false
        }
    }
    return true; // Если все числа чётные, возвращаем true
}

console.log(f(str2)); // true

