// 3. Дан массив со числами. Удалите из него числа, имеющие два и более нуля.

const arr = [100, 2, 23, 30, 1002, 5000];

for (let i = arr.length - 1; i >= 0; i--) {
    const zeroCount = arr[i].toString().split('0').length - 1; // Подсчитываем количество нулей
    if (zeroCount >= 2) {
        arr.splice(i, 1); // Удаляем элементы с двумя или более нулями
    }
}

console.log(arr); // [2, 23, 30]

