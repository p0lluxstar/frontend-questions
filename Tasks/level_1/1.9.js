// 4. Найдите все числа от 1 до 1000, сумма цифр которых равна 13.

for (let i = 1; i <= 1000; i++) {
    const numStr = i.toString(); // Преобразуем число в строку
    let sum = 0;
    
    // Проходим по каждой цифре числа
    for (let num of numStr) {
        sum += parseInt(num); // Суммируем цифры
    }

    // Если сумма цифр равна 13, выводим число
    if (sum === 13) {
        console.log(i);
    }
}

